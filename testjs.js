// Заголовок X-Frame-Options со стороны сервера может разрешать
//  или запрещать отображение страницы внутри фрейма.


// Заголовок может иметь 3 значения:
// DENY – Никогда не показывать страницу внутри фрейма.
// SAMEORIGIN – Разрешить открытие страницы внутри фрейма только в
//  том случае, если родительский документ имеет тот же источник.
// ALLOW-FROM domain – Разрешить открытие страницы внутри фрейма
//  только в том случае, если родительский документ находится на указанном в заголовке домене.
// Например, Twitter использует X-Frame-Options: SAMEORIGIN.



